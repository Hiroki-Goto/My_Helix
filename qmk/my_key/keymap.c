bool process_record_user(uint16_t keycode, keyrecord_t *record){
	if (record->event.pressed){
		switch(keycode){
			case quit:
				SEND_STRING(":q"); // this is our macro!
			return false;
			
			case save:
				SEND_STRING(":w"); // this is our macro!
			return false;
			case sq:
				SEND_STRING(":wq"); // this is our macro!
			return false;
			case tree:
				SEND_STRING(":needTree"); // this is our macro!
			return false;

			case osewa:
				SEND_STRING("osehaninatteorimasu"); // this is our macro!
			return false;
			case gotou:
				SEND_STRING("gotoudesu"); // this is our macro!
			return false;
			case yoroshiku:
				SEND_STRING("yoroshikuonegaishimasu"); // this is our macro!
			return false;

		}
	}
	return true;
};
